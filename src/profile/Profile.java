package profile;

public class Profile {
	
	private int profileID;
	private String userID;
	private String day;
	private String job;
	private String userPhone;
	private String sns;
	private String contents;
	private int profileAvailable;
	private String userSchoolSerialNumber;
	private String Year;
	private String userName;
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserSchoolSerialNumber() {
		return userSchoolSerialNumber;
	}
	public void setUserSchoolSerialNumber(String userSchoolSerialNumber) {
		this.userSchoolSerialNumber = userSchoolSerialNumber;
	}
	public String getYear() {
		return Year;
	}
	public void setYear(String year) {
		Year = year;
	}
	public int getProfileID() {
		return profileID;
	}
	public void setProfileID(int profileID) {
		this.profileID = profileID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getUserPhone() {
		return userPhone;
	}
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	public String getSns() {
		return sns;
	}
	public void setSns(String sns) {
		this.sns = sns;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public int getProfileAvailable() {
		return profileAvailable;
	}
	public void setProfileAvailable(int profileAvailable) {
		this.profileAvailable = profileAvailable;
	}
	
	
}
