package user;

public class User {
	private String userID;
	private String userPassword;
	private String userName;
	private String userEmail;
	private String userSchoolSerialNumber;
	private String userPhone;
	private String userSchoolNumber;
	private String graduateYear;
	
	public String getGraduateYear() {
		return graduateYear;
	}
	public void setGraduateYear(String graduateYear) {
		this.graduateYear = graduateYear;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserSchoolSerialNumber() {
		return userSchoolSerialNumber;
	}
	public void setUserSchoolSerialNumber(String userSchoolSerialNumber) {
		this.userSchoolSerialNumber = userSchoolSerialNumber;
	}
	public String getUserPhone() {
		return userPhone;
	}
	public void setUserPhone(String userPhone) {
		this.userPhone = userPhone;
	}
	public String getUserSchoolNumber() {
		return userSchoolNumber;
	}
	public void setUserSchoolNumber(String userSchoolNumber) {
		this.userSchoolNumber = userSchoolNumber;
	}

	
	
}
