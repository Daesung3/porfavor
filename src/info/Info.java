package info;

public class Info {
	private String userSchoolSerialNumber;
	private String userSchoolNumber;
	private String userName;
	private String year;
	private String personalPhoto;
	public String getUserSchoolSerialNumber() {
		return userSchoolSerialNumber;
	}
	public void setUserSchoolSerialNumber(String userSchoolSerialNumber) {
		this.userSchoolSerialNumber = userSchoolSerialNumber;
	}
	public String getUserSchoolNumber() {
		return userSchoolNumber;
	}
	public void setUserSchoolNumber(String userSchoolNumber) {
		this.userSchoolNumber = userSchoolNumber;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getPersonalPhoto() {
		return personalPhoto;
	}
	public void setPersonalPhoto(String personalPhoto) {
		this.personalPhoto = personalPhoto;
	}
	

}
